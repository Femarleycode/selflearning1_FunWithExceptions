package main;

public class Methods {
	
	public double add(double x, double y) {
		double z;
		z = x+y;
		return z;
	}
	public double sub(double x, double y) {
		double z;
		z = x-y;
		return z;
	}
	public double multi(double x, double y) {
		double z;
		z = x*y;
		return z;
	}
	public double div(double x, double y) {
		double z;
		z = x/y;
		return z;
	}
}
